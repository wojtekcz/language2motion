    public static func spectralColor(c: Float) -> RGB {
        /// c (0.0...1.0)
        let scCount = spectralRGB.count

        var c2: Float = c
        
        if c2>0.0 {
            c2 = min(c, 1.0)
        } else if c2 < 0.0 {
            c2 = max(c, 0.0)
        }

        let idx: Int = Int(c2*Float(scCount-1))
        let color = spectralRGB[idx]
        return RGB(r: color[0], g: color[1], b: color[2])
    }
